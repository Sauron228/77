package hometask6;

public class CycleDoWhile {
    public static void main(String[] args) {
        xX();
    }

    public static void xX() {
        int x = 0;
        do {
            if (x <= 10) {
                System.out.println("x =" + x);
            }
            x++;
        } while (x <= 10);
    }
}





